    function Desk(id){

        var tanggal = $('#tanggal').val();
        var type = $('#type').val();
        var dari = $('#dari').val();
        var sampai = $('#sampai').val();

        if(type == 'hour'){
            if(tanggal == '' || dari == '' || sampai == ''){
                swal({
                    title: "Perhatikan",
                    text: "Harap isi Tanggal atau Waktu dulu!",
                    icon: "error",
                    buttons: false,
                    timer: 2000,
                });
            } else {
                $('.available').attr("src", "../assets/template/karawaci/tombol1.png");
                $('#imgdesk'+id).attr("src", "../assets/template/karawaci/tombol"+id+"_p.png");

                $.ajax({
                  type: 'POST',
                  url: "{{ route('workingspace.pilih') }}",
                  data: {
                      '_token': $('input[name=_token]').val(),
                      'id': id,
                  },
                  success: function(data) {

                    $('#pilihdesk').html(data.desk_code);
                    $('#valuedesk').val(data.id);
                    $('#namadesk').val(data.desk_code);

                  }

                });
            }


        } else {

            if(tanggal == ''){
                swal({
                    title: "Perhatikan",
                    text: "Harap isi Tanggal terlebih dahulu!",
                    icon: "error",
                    buttons: false,
                    timer: 2000,
                });
            } else {
                $('.available').attr("src", "../assets/template/karawaci/tombol1.png");
                $('#imgdesk'+id).attr("src", "../assets/template/karawaci/tombol"+id+"_p.png");

                $.ajax({
                  type: 'POST',
                  url: "{{ route('workingspace.pilih') }}",
                  data: {
                      '_token': $('input[name=_token]').val(),
                      'id': id,
                  },
                  success: function(data) {

                    $('#pilihdesk').html(data.desk_code);
                    $('#valuedesk').val(data.id);
                    $('#namadesk').val(data.desk_code);
                  }

                });
            }

        }

        
    }


</script>